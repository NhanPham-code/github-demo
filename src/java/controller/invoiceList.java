    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        Cookie[] cookies = request.getCookies();
        String user = null;
        if (cookies != null) {
            for (Cookie cookie : cookies) {
                if (cookie.getName().equals("username")) {
                    user = cookie.getValue();
                }
            }
        }
        request.setAttribute("user", user);

        ProductDAO pDAO= new ProductDAO();
        List<String> categoryList = pDAO.getAllType();
        request.setAttribute("categoryList", categoryList);
        
        
        invoiceDAO idao = new invoiceDAO();
        List<invoice> listInvoice = new ArrayList<>();
        listInvoice=idao.getAllInvoice();
        request.setAttribute("invoiceList", listInvoice);
        
        float total =0;
        for(int i =0; i<listInvoice.size(); i++){
            total+=listInvoice.get(i).getTotal();
        }
        request.setAttribute("total", total);
        
        request.getRequestDispatcher("listInvoice.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

